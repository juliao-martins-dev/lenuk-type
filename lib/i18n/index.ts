function getInitialLanguage(): UILanguageCode {
  if (typeof window === "undefined") return "en";
  const saved = localStorage.getItem(STORAGE_KEY_UI_LANG);
  const valid: UILanguageCode[] = ["en", "ar", "vi", "ru", "id", "tet"];
  return valid.includes(saved as UILanguageCode) ? (saved as UILanguageCode) : "en";
}

